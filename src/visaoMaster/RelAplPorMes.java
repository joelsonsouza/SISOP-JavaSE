     public void CbAno(){
    
        conex.conexao();
        conex.executaSql("select DISTINCT ano  from tbl_regmov order by ano desc ");
        try{
            conex.rs.first();
            jComboBoxAno.removeAllItems();
            do{
                jComboBoxAno.addItem(conex.rs.getString("ano").toUpperCase());
            }while(conex.rs.next());
        
        
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Nenhum registro encontrado\n"+ex);
            //RegistrarAtiv.this.dispose();
            }
        }
    
    public void CbMes(){
    
        conex.conexao();
        conex.executaSql("select DISTINCT mes  from tbl_regmov where id_tipo='20'"); //20 - FUND DE INVESTIMENTO
        try{
            conex.rs.first();
            jComboBoxMes.removeAllItems();
            do{
                jComboBoxMes.addItem(conex.rs.getString("mes").toUpperCase());
            }while(conex.rs.next());
        
        
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Nenhum registro encontrado\n"+ex);
            //RegistrarAtiv.this.dispose();
            }
        }
