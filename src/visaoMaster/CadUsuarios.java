    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        
     if(jTextFieldNome.getText().isEmpty()|| jTextFieldLogin.getText().isEmpty() || jPasswordFieldSenha.getText().isEmpty()) { 
 
            if(jTextFieldNome.getText().isEmpty()){ 
            jLabelNomeObg.setVisible(true);
            //JOptionPane.showMessageDialog(null, "Campo Obrigatório");
            jTextFieldNome.requestFocus();  
            }else{
            jLabelNomeObg.setVisible(false);
            }
            if(jTextFieldLogin.getText().isEmpty()) { 
            jLabelLoginObg.setVisible(true);
            //JOptionPane.showMessageDialog(null, "Campo Obrigatório");
            jTextFieldLogin.requestFocus(); 
            }else{
            jLabelLoginObg.setVisible(false);
            }
            if(jPasswordFieldSenha.getText().isEmpty()) { 
            jLabelSenhaObg.setVisible(true);
            jPasswordFieldSenha.requestFocus();  
            //JOptionPane.showMessageDialog(null, "Campo Obrigatório");
            }else{
            jLabelSenhaObg.setVisible(false);    
            }        
        }else{
            mod.setNome(jTextFieldNome.getText());
            mod.setLogin(jTextFieldLogin.getText());
            mod.setSenha(jPasswordFieldSenha.getText());
            mod.setEmail(jTextFielEmail.getText());
            mod.setPerfil((String) jComboBoxPerfil.getSelectedItem()); 
            mod.setCelular(jFormattedTextFieldCel.getText());
            mod.setAtivo(jCheckBoxUserAtivar.isSelected());
            control.Editar(mod);
            jLabelSenhaObg.setVisible(false);
            jLabelLoginObg.setVisible(false);
            jLabelNomeObg.setVisible(false); 
            jButtonIncluir.setEnabled(false);
            jButtonApagar.setEnabled(true);
            jButtonEditar.setEnabled(true);
            jTextFieldSenhaRev.setText("");
        jButtonRevelaSenha.setVisible(true);
        jLabelMostSenha.setVisible(false);
        }   
        
       
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonApagarActionPerformed
        
            int Confirm = JOptionPane.showConfirmDialog(null,"Excluir registro?"," Excluir", JOptionPane.YES_NO_OPTION);
            if (Confirm == JOptionPane.YES_OPTION) {
            mod.setCodigo(Integer.parseInt(jTextFieldCod.getText()));
            mod.setNome(jTextFieldNome.getText());
            control.Excluir(mod);
            
            jTextFieldCod.setText("");
            jTextFieldNome.setText("");
            jTextFielEmail.setText("");
            jTextFieldLogin.setText("");
            jFormattedTextFieldCel.setText("");
            jPasswordFieldSenha.setText("");
            jCheckBoxUserAtivar.setSelected(false);
            jButtonIncluir.setEnabled(true);
            jButtonApagar.setEnabled(false);
            jButtonEditar.setEnabled(false);
            jTextFieldSenhaRev.setText("");
            jButtonRevelaSenha.setVisible(false);
            jLabelMostSenha.setVisible(false);
            
            }else if (Confirm == JOptionPane.NO_OPTION){
                setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            }
     
        

    }//GEN-LAST:event_jButtonApagarActionPerformed

