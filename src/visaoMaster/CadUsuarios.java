public void PreencherTabelaIF(String SQL){
      ArrayList dados = new ArrayList();
      String[] colunas = new String[]{"COD","PLACA","MODELO","STATUS"};
      
      conex.conexao();
      conex.executaSql(SQL);

      try{
          conex.rs.first();       
          do{
              dados.add(new Object[]{conex.rs.getInt("id_usuario"),
                                     conex.rs.getString("nome"),
                                     conex.rs.getString("usuario"),
                                     conex.rs.getString("perfil")
                                   
                                    });
 
          }while(conex.rs.next());
      
        }catch(SQLException ex){
          JOptionPane.showMessageDialog(null, "Erro ao preencher ArrayList\nErro:"+ex);
        }
          
     BeansTabelas modelo = new BeansTabelas(dados, colunas);
      
     jTableUsuario.setModel(modelo);
     jTableUsuario.getColumnModel().getColumn(0).setPreferredWidth(40);
     jTableUsuario.getColumnModel().getColumn(0).setResizable(false);
     jTableUsuario.getColumnModel().getColumn(0).setHeaderValue("COD");
     jTableUsuario.getColumnModel().getColumn(1).setPreferredWidth(181);
     jTableUsuario.getColumnModel().getColumn(1).setResizable(false); //não redimensionaveL
     jTableUsuario.getColumnModel().getColumn(1).setHeaderValue("NOME");
     jTableUsuario.getColumnModel().getColumn(2).setPreferredWidth(80);
     jTableUsuario.getColumnModel().getColumn(2).setResizable(false); //não redimensionaveL
     jTableUsuario.getColumnModel().getColumn(2).setHeaderValue("LOGIN");
     jTableUsuario.getColumnModel().getColumn(3).setPreferredWidth(115);
     jTableUsuario.getColumnModel().getColumn(3).setResizable(false); //não redimensionaveL
     jTableUsuario.getColumnModel().getColumn(3).setHeaderValue("PERFIL");
           

     jTableUsuario.getTableHeader().setReorderingAllowed(false); //não reordenavel
     jTableUsuario.setAutoResizeMode(jTableUsuario.AUTO_RESIZE_OFF); //não redmencionar
     jTableUsuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//selicioar um dado por vez
    }



