    public void CbCorrentista(){
    
        conex.conexao();
        conex.executaSql("select *from tbl_corretista where status=true order by nomecorretista");
        try{
            conex.rs.first();
            jComboBoxCorrentista.removeAllItems();
            do{
                jComboBoxCorrentista.addItem(conex.rs.getString("nomecorretista").toUpperCase());
            }while(conex.rs.next());
        
        
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Registre os correntistas  no menu Cadastro de Correntista");
            //RegistrarAtiv.this.dispose();
            }
        }
    
    public void CbBank(){
    
        conex.conexao();
        conex.executaSql("select * from tbl_bank");
        try{
            conex.rs.first();
            jComboBoxBanco.removeAllItems();
            do{
                jComboBoxBanco.addItem(conex.rs.getString("nomebank").toUpperCase());
            }while(conex.rs.next());
        
        
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Entre em contato com o desenvolvedor do sistema!\n"+ex);
            //RegistrarAtiv.this.dispose();
            }
        }
    
    public void PreencherTabela(String SQL){
      ArrayList dados = new ArrayList();
      String[] colunas = new String[]{"","","",""};
      
      conex.conexao();
      conex.executaSql(SQL);

      try{
          conex.rs.first();       
          do{
              dados.add(new Object[]{conex.rs.getInt("id_contbank"),
                                     conex.rs.getString("nomebank"),
                                     conex.rs.getString("tipocard"),                                     
                                     conex.rs.getString("nuncard"),
                                     
                                    });
 
          }while(conex.rs.next());
      
        }catch(SQLException ex){
 
           // JOptionPane.showMessageDialog(rootPane, "Não foram encontrados registros para os filtros selecionados!\nSelecione opção válida");
            
          //JOptionPane.showMessageDialog(rootPane, "Erro ao preencher ArrayList\nErro:"+ex); 
        }
      
      BeansTabelas modelo = new BeansTabelas(dados, colunas);
      
      jTable1.setModel(modelo);
      jTable1.getColumnModel().getColumn(0).setPreferredWidth(30);
      jTable1.getColumnModel().getColumn(0).setResizable(false);
      jTable1.getColumnModel().getColumn(0).setHeaderValue("ID");
      jTable1.getColumnModel().getColumn(1).setPreferredWidth(120);
      jTable1.getColumnModel().getColumn(1).setResizable(false);
      jTable1.getColumnModel().getColumn(1).setHeaderValue("BANCO");
      jTable1.getColumnModel().getColumn(2).setPreferredWidth(146);
      jTable1.getColumnModel().getColumn(2).setResizable(false); //não redimensionaveL
      jTable1.getColumnModel().getColumn(2).setHeaderValue("TIPO");
      jTable1.getColumnModel().getColumn(3).setPreferredWidth(197);
      jTable1.getColumnModel().getColumn(3).setResizable(false);
      jTable1.getColumnModel().getColumn(3).setHeaderValue("NÚMERO");
      

      jTable1.getTableHeader().setReorderingAllowed(false); //não reordenavel
      //jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_OFF); //não redmencionar
      jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//selicioar um dado por vez
    }
    
    
