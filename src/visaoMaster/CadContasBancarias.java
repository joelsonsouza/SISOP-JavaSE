    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
        
        if(jTextFieldNomeCartao.getText().isEmpty() || jTextFieldNumCartao.getText().isEmpty() || jDateChooserVnc.getDate() == null || jTextFieldCvv.getText().isEmpty() ){
        if(jTextFieldCvv.getText().isEmpty()){
            jTextFieldCvv.requestFocus();
        }
        if(jDateChooserVnc.getDate() == null){
            jDateChooserVnc.requestFocusInWindow();
        }
        if(jTextFieldNumCartao.getText().isEmpty()){
            jTextFieldNumCartao.requestFocus();
        }
        
        if(jTextFieldNomeCartao.getText().isEmpty()){
            jTextFieldNomeCartao.requestFocus();
        }

        }else{

        mod.setCorrentista((String) jComboBoxCorrentista.getSelectedItem());
        mod.setTipodeconta((String) jComboBoxTconta.getSelectedItem());
        mod.setBanco((String) jComboBoxBanco.getSelectedItem());
        mod.setTcard((String) jComboBoxTcatao.getSelectedItem());
        mod.setNomecard(jTextFieldNomeCartao.getText());
        mod.setNumcard(jTextFieldNumCartao.getText());
        mod.setAgencont(jTextFieldAgenCont.getText());
        mod.setVenccarad(jDateChooserVnc.getDate());
        mod.setCvvcard(jTextFieldCvv.getText());
        control.Salvar(mod);
        jButtonIncluir.setEnabled(false);
        jButtonEditar.setEnabled(true);
        jButtonApagar.setEnabled(true);
        
        }
          
    }//GEN-LAST:event_jButtonIncluirActionPerformed

